const findKeyByValue = function(bestTv, movie) {
  for (let genre in bestTv) {
    if (bestTv[genre] === movie) {
      return genre;
    }
  }
  return undefined;
};

const bestTVShowsByGenre = {
  sci_fi: "The Expanse",
  comedy: "Brooklyn Nine-Nine",
  drama:  "The Wire"
};

